				§ 3. Prawa i obowiązki Centrum Językowego SCOLA
			</div>
			<ol class='header'>
				<li>CJS zapewnia słuchaczom naukę w grupach 5-8 oraz 9-12 osobowych lub indywidualnie zgodnie z cennikiem dostępnym w biurze jak również na stronie internetowej szkoły. </li>
				<li>CJS podczas prowadzonych zapisów ma obowiązek zbadania potrzeb osób zapisujących się na kursy językowe uzyskując odpowiedzi na pytania:<br>
-Na jakim efekcie Panu zależy?<br>
- Co dzięki….. chciałby Pan osiągnąć?<br>
- Czego chciałby Pan uniknąć?<br>
- Co miałby zawierać kurs ….,aby spełnił Pana oczekiwania?</li>
				<li>Przed rozpoczęciem kursu CJS jest zobowiązane przekazać kursantom informację nt. tego, jaki poziom zaawansowania znajomości języka osiągną po zakończeniu kursu oraz jaki zakres tematyczny zostanie zrealizowany.</li>
				<li>CJS zapewnia kursantom w ramach uczestnictwa w zajęciach bezpłatne materiały dydaktyczne.</li>
				<li>CJS ma obowiązek poinformować Słuchaczy o dokładnej liczbie godzin zajęć pozostałych do końca kursu oraz zobowiązuje się prowadzić rzetelny wykaz zrealizowanych lekcji.</li>
				<li>W przypadku odwołania zajęć lektor ma obowiązek ustalenia innego terminu lekcji wraz ze Słuchaczami lub przeniesienia odwołanych godzin na koniec kursu.</li>
				<li>CJS zobowiązuje się do informowania swoich Słuchaczy o ewentualnym odwołaniu zajęć oraz do przekazania innych ważnych informacji telefonicznie lub poprzez e-mail wskazany przez Kursanta w Umowie. W wyjątkowych przypadkach CJS zastrzega sobie prawo odwołania zajęć w trybie natychmiastowym. W takim przypadku zajęcia zostaną zrealizowane w innym ustalonym ze Słuchaczami terminie.</li>
				<li>CJS ma prawo wykreślić słuchacza z kursu w przypadkach rażącego łamania postanowień regulaminu, w szczególności w przypadku zalegania z płatnościami lub rejestrowaniem zajęć lekcyjnych bez zgody lektora.</li>
				<li>CJS nie ma obowiązku zmiany harmonogramu zajęć ze względu na indywidualną prośbę słuchacza. Zmiana taka może nastąpić tylko po uprzednim ustaleniu tej kwestii z lektorem i pozostałymi członkami grupy.</li>
				<li>Szkoła ponosi odpowiedzialność za bezpieczeństwo dzieci i młodzieży tylko podczas zajęć na terenie sal wykładowych szkoły. Za bezpieczeństwo w drodze na zajęcia i w drodze powrotnej jak również podczas oczekiwania na zajęcia odpowiedzialność ponoszą rodzice /opiekunowie.</li>
				<li>W przypadku zmniejszenia się liczby osób w grupie do 4 kursantów, CJS ma prawo dokonać rozwiązania grupy bez ponoszenia dodatkowych roszczeń na rzecz Słuchaczy lub zwiększenia opłaty za kurs po uprzednim ustaleniu z kursantami.</li>
				<li>Szkoła zastrzega sobie prawo do zmiany lektorów w trakcie kursu z ważnych powodów organizacyjnych.</li>
				<li>W przypadku realizacji zajęć w salach wyposażonych w tablice interaktywne, wszyscy słuchacze kursu otrzymują lekcje na wskazane przez nich w Umowie adresy e-mail.</li>
				<li>Zakończenie nauki jednego poziomu zaawansowania następuje poprzez napisanie egzaminu końcowego. Egzamin może mieć charakter pisemny lub też ustny. Kontynuowanie kolejnego poziomu nie zwalnia kursanta z obowiązku napisania egzaminu.</li>
				<li>Po napisaniu egzaminu przez Słuchacza, Szkoła zobowiązuje się do wydania certyfikatu. Certyfikat zawiera informację na temat kursu, okresu jego realizacji, uzyskanego poziomu, końcowego wyniku procentowego i/lub ocenie. Certyfikat jest wydawany w języku w jakim realizowany jest kurs. Na wniosek kursanta szkoła może wydać nieodpłatnie certyfikat także w języku polskim.</li>
				<li>Końcowy wynik procentowy odpowiada ocenie w sposób następujący:
					<ol>
						<li>100% - 85% - ocena 5.0</li>
						<li>84% - 70% - ocena 4.0</li>
						<li>69% - 50% - ocena 3.0</li>
						
					</ol>
				</li>
				<li>CJS na zakończenie kursu ma obowiązek przeprowadzić ewaluację na temat jakości zrealizowanego szkolenia, co nie jest obligatoryjne względem kursanta.</li>
				<li>CJS na bieżąco aktualizuje ofertę szkoleniową w zależności od zmian w programach i standardach kształcenia, jak również od zmieniających się potrzeb odbiorców szkoleń i potrzeb rynku.</li>
				
			</ol>
			
