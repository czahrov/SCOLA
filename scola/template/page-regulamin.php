				§4. Płatności
			</div>
			<ol class='header'>
				<li>Każdy Słuchacz przy zapisie informowany jest o cenie kursu, zgodnie z cennikiem ustalonym przed rozpoczęciem szkolenia, z uwzględnieniem przysługujących mu ewentualnych rabatów. Słuchacz jest zobowiązany do dokonywania płatności zgodnie z otrzymaną wyceną.</li>
				<li>Cena za kurs uzależniona jest od ilości osób w grupie oraz od poziomu zaawansowania, co zostało określone w aktualnym cenniku CJS.</li>
				<li>Cena zawiera test kontrolny, określoną ilość godzin kursu, materiały dydaktyczne, egzamin końcowy oraz certyfika.</li>
				<li>W przypadku dołączenia słuchacza do grupy w trakcie trwania kursu oraz jednoczesnej zmianie progu cenowego, CJS zobowiązuje się obniżyć stawkę godziny lekcyjnej grupy adekwatnej do ilości osób na kursie. Obniżenie ceny ma miejsce w miesiącu następującym po miesiącu, w którym zastąpiło zwiększenie liczby osób w grupie.</li>
				<li>Opuszczenie zajęć przez Słuchacza nie skutkuje obniżeniem miesięcznej raty za kurs. </li>
				<li>Słuchacz ma prawo do wpłaty całej należnej kwoty za kurs z góry lub do w określonych ratach miesięcznych. System ratalny nie wpływa na zwiększenie ceny kursu.</li>
				<li>Słuchacze uiszczający opłaty w systemie ratalnym mają obowiązek dokonania ich do 10 dnia danego miesiąca (z góry). W przypadku nieterminowego wnoszenia opłat CJS zastrzega sobie prawo do pobierania ustawowych odsetek za opóźnienie.</li>
				<li>Opłaty za kursy przyjmowane są w biurze CJS lub w formie przelewów na konto firmy wskazane na rachunku. W tytule przelewu należy podać imię i nazwisko Słuchacza, grupę oraz numer raty za którą zostaje dokonany przelew.</li>
				<li>Jeżeli uczestnikami kursu są osoby niepełnoletnie, rachunek zostaje wypisany na jednego z rodziców lub na innego opiekuna prawnego ucznia.</li>
				<li>W szczególnych przypadkach istnieje możliwość indywidualnego ustalenia terminów płatności po wcześniejszym uzgodnieniu tego z kierownictwem Szkoły, co wymaga formy pisemnej.</li>
				
			</ol>
			
