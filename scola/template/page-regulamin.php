				§ 2. Prawa i obowiązki słuchacza
			</div>
			<ol class='header'>
				<li>Zapisanie się do Centrum Językowego SCOLA jest równoznaczne z udostępnieniem danych osobowych na potrzeby kontaktu ze Słuchaczami (numer telefonu, adres zamieszkania oraz adres e-mail) a także numer i seria dowodu osobistego.</li>
				<li>Każdy ze Słuchaczy wyraża zgodę na przetwarzanie danych osobowych na potrzeby badania jakości szkoleń.</li>
				<li>Kursant ma prawo do uzyskania informacji na temat postępów w nauce, w tym do otrzymania zaświadczenia o uczestnictwie w kursie na każdym etapie nauki.</li>
				<li>Słuchacz ma obowiązek pisania okresowych testów sprawdzających wiedzę, jak również quizów i testów z bieżącego materiału.</li>
				<li>Kursant który dołącza do grupy w trakcie trwania szkolenia zobowiązuje się dostosować do grafiku ustalonego przez grupę na pierwszych zajęciach.</li>
				<li>Słuchacz ma prawo do rezygnacji z kursu do 7 dni od daty podpisania umowy chyba, że umowa przewiduje inaczej. Wówczas rezygnację należy złożyć najpóźniej 1 miesiąc przed planowanym zakończeniem szkolenia. Rezygnację składa się w formie pisemnej.</li>
				<li>W przypadkach losowych rezygnację można złożyć w formie telefonicznej.</li>
				<li>Kursant, który rezygnuje z kursu w okresie do 7 dni od jego rozpoczęcia zobowiązany jest uiścić opłatę za zrealizowane godziny kursu.</li>
				<li>Kursant, który rezygnuje z kursu po okresie wymienionym w pkt. 8 zobowiązany jest uiścić opłatę za pozostałe godziny kursu. Należność można regulować w ratach miesięcznych a ostatnia rata zostanie wpłacona przed zakończeniem kursu przez grupę.</li>
				<li>Słuchacz ponosi materialną odpowiedzialność za dokonane szkody i zniszczenia mienia CJS oraz mienia innych uczestników zajęć.</li>
				<li>Słuchacz ponosi odpowiedzialność za ewentualne szkody i następstwa szkód wyrządzonych osobom trzecim w trakcie lub w związku z przebywaniem na terenie siedziby CJS. </li>
				<li>Uczeń ma obowiązek przestrzegania przepisów BHP w czasie przebywania na terenie siedziby CJS oraz w innych miejscach, w których odbywają się organizowane przez szkołę zajęcia.</li>
				<li>W przypadku zgłoszonej nieobecności na zajęciach Słuchacz ma prawo do skorzystania z bezpłatnych konsultacji z lektorem na 10 min przed kolejnymi zajęciami. Nieobecności na zajęciach nie stanowią podstawy do zwrotu opłaty, niezależnie od powodu lub czasu trwania nieobecności.</li>
				<li>Prawo do bezpłatnych konsultacji mają również osoby, które mają problemy z opanowaniem materiału kursowego.</li>
				<li>Kursant ma prawo do otrzymania materiałów dydaktycznych wykorzystanych na kursie podczas jego nieobecności.</li>
				<li>Kursant zobowiązuje się do systematycznego uczestnictwa w zajęciach.</li>
				<li>W CJS na każdym kursie liczącym 120 godzin przeprowadzane jest minimum 6 testów śródokresowych. Wyniki testów cząstkowych stanowią 30% wyniku końcowego.</li>
				<li>W przypadku uczniów niepełnoletnich, rodzic /opiekun prawny ma możliwość wglądu w testy cząstkowe pisane przez kursanta.</li>
				<li>Na zakończenie kursu każdy Słuchacz ma prawo do uzyskania wewnętrznego certyfikatu językowego, który jest wydawany po napisaniu przez Kursanta egzaminu końcowego.</li>
				<li>W przypadku nieobecności Kursanta w dniu egzaminu, Słuchacz może przystąpić do egzaminu w innym terminie ustalonym wcześniej z uprawnionym przedstawicielem CJS.</li>
				<li>W przypadku uzyskania z egzaminu wyniku mniejszego niż 50%, Kursant ma możliwość przystąpienia do egzaminu w drugim terminie.</li>
				<li>Warunkiem kontynuacji nauki na kursie o wyższym poziomie zaawansowania jest zaliczenie stopnia niższego z wynikiem pozytywnym (minimum 50%).</li>
				<li>Słuchacz ma prawo do zgłaszania swoich uwag na temat jakości świadczonych usług przez szkołę w formie ustnej lub pisemnej podczas rozmowy z uprawnionym przedstawicielem CJS.</li>
				
			</ol>
			
