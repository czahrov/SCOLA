				§1. Warunki uczestnictwa w zajęciach:
			</div>
			<ol class='header'>
				<li>W zajęciach w Centrum Językowym SCOLA może uczestniczyć Słuchacz, który:
					<ol>
						<li>
							zapoznał się z niniejszym regulaminem i zobowiązał się do jego przestrzegania<br>
							(w przypadku uczniów niepełnoletnich obowiązek zapoznania się z regulaminem dotyczy rodziców /opiekunów prawnych ucznia), 
						</li>
						<li>
							zaakceptował warunki zawarte w Umowie i opatrzył ją własnoręcznym podpisem a w przypadku osób niepełnoletnich Umowa ma zostać podpisana przez rodzica /opiekuna prawnego,
						</li>
						<li>
							nie zalega z regulowaniem należności, 
						</li>
						<li>
							stosuje się do zasad regulaminu, 
						</li>
						<li>
							bierze systematycznie udział w zajęciach. 
						</li>
						
					</ol>
					
				</li>
				<li>Nowi Słuchacze są przydzielani do poszczególnych grup językowych według kategorii wiekowych i stopnia opanowania języka.</li>
				<li>
					W Centrum Językowym SCOLA Słuchacz ma możliwość zapisu do jednej z poniższych grup wiekowych:
					<ol>
						<li>dzieci w wieku przedszkolnym,</li>
						<li>dzieci szkół podstawowych,</li>
						<li>dzieci szkół gimnazjalnych,</li>
						<li>grupy osób dorosłych (od uczniów klas licealnych).</li>
						
					</ol>
				</li>
				<li>Każdy nowy Słuchacz przystępujący do zajęć na wyższym stopniu zaawansowania niż początkujący zobowiązany jest do napisania nieodpłatnego testu kwalifikującego i odbycia ewentualnej rozmowy z lektorem, za pomocą których oceniony jest stopień zaawansowania językowego. Na podstawie wyniku testu kwalifikacyjnego proponowane jest uczestnictwo w odpowiedniej według poziomu grupie.</li>
				<li>O ewentualnej zmianie przydzielonej grupy każdorazowo decyduje nauczyciel w porozumieniu z kierownictwem CJS. </li>
				<li>W CJS istnieje możliwość dołączenia do grupy w trakcie trwania kursu. Słuchacz dokonuje opłaty wyłącznie za ilość zajęć, która pozostała do zakończenia kursu.</li>
				<li>Szkoła prowadzi zapisy przez cały rok. Kursy rozpoczynają się w przypadku zgłoszenia się minimum pięciu słuchaczy na ten sam poziom.</li>
				
			</ol>
			
