				§5. Reklamacja usługi szkoleniowej
			</div>
			<ol class='header'>
				<li>Prawo złożenia reklamacji przysługuje każdemu Kursantowi (w przypadku szkoleń otwartych) lub Klientowi (w przypadku szkoleń zamkniętych).</li>
				<li>Reklamacja może dotyczyć kwestii merytorycznych i/lub organizacyjnych.</li>
				<li>Reklamacja odbywa w formie pisemnej lub ustnej podczas rozmowy z właścicielem CJS.</li>
				<li>Każda reklamacja podlega rejestracji i służy do oceny działania firmy dokonywanej corocznie przez właściciela CJS.</li>
				<li>W przypadku, gdy CJS nie spełni oczekiwań kursanta odnośnie osiągniętych postępów, Kursantowi przysługuje prawo do skorzystania z dodatkowych 5 godzin szkoleniowych kursu w ramach zajęć indywidualnych.</li>
				
			</ol>
			
