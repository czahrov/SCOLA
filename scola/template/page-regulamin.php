				§6. Przepisy porządkowe
			</div>
			<ol class='header'>
				<li>Na terenie CJS obowiązuje bezwzględny zakaz palenia papierosów, spożywania alkoholu i substancji niedozwolonych.</li>
				<li>Słuchacz zobowiązany jest do przestrzegania zasad porządku publicznego. </li>
				<li>Słuchacz oczekujący na zajęcia zobowiązany jest do zachowania ciszy.</li>
				<li>Na czas trwania zajęć słuchacze zobowiązani są do wyłączenia bądź wyciszenia telefonów komórkowych i innych urządzeń multimedialnych tak, aby nie zakłócać przebiegu zajęć innym uczestnikom.</li>
				<li>Na zajęciach zabrania się fotografowania oraz nagrywania w jakikolwiek sposób pracowników szkoły oraz lekcji przez nich prowadzonych.</li>
				<li>W razie nie przestrzegania powyższych przepisów porządkowych lub w razie naruszenia innych norm dyscyplinarnych Słuchacz może zostać skreślony z listy uczestników kursu.</li>
				
			</ol>
			
