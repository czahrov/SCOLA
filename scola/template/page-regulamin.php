				§7. Postanowienia końcowe
			</div>
			<ol class='header'>
				<li>Wraz ze złożeniem podpisu na Umowie niniejszy Regulamin kształtuje treść stosunku prawnego (wzajemnych praw i obowiązków) pomiędzy słuchaczem a szkołą.</li>
				<li>Przed przystąpieniem do uczestnictwa w zajęciach Słuchacz zobowiązuje się do zapoznania z Regulaminem Centrum Językowego SCOLA oraz cennikiem i terminami płatności.</li>
				<li>W sprawach nieuregulowanych niniejszym regulaminem mają zastosowane przepisy Kodeksu Cywilnego.</li>
				
			</ol>
			
