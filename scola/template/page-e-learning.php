Znak Jakości Małopolskich Standardów Usług Edukacyjno-Szkoleniowych (MSUES) przyznany Centrum Językowemu SCOLA
W sierpniu 2017 roku Centrum Językowe SCOLA uzyskało Znak Jakości Małopolskich Standardów Edukacyjno – Szkoleniowych.
Znak jakości MSUES przyznawany jest przez Wojewódzki Urząd Pracy w Krakowie w ramach Centrum Zapewnienia Jakości Kształcenia w Małopolsce. Otrzymują go instytucje szkoleniowe, które pozytywnie przeszły zewnętrzny audyt, będący ostatnim etapem procesu weryfikacji pod kątem spełniania MSUES. Znak jakości jest gwarancją wysokiej jakości usług, jakie świadczy wyróżniona instytucja szkoleniowa – potwierdza zgodność oferty z potrzebami i oczekiwaniami Klientów, przygotowanie merytoryczne i metodyczne trenerów, rzetelność przekazywanych treści oraz różnorodność form nauczania. Dzięki Znakowi Jakości MSUES Centrum Językowe SCOLA dołączyło grona instytucji, w których można realizować bony szkoleniowe w ramach realizowanych szkoleń, co daje możliwość korzystania z dofinansowań kursów językowych ze środków UE.

