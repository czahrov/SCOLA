				<li>3 rodzaje tekstów </li>
				<li>- Exposition (opis tematu) </li>
				<li>- Argumentation (przedstawienie opinii i argumentów popierających) </li>
				<li>- Historical biographical/event narrative (opis wydarzenia lub czyjegoś życia opisany przez inną osobę) </li>
				<li>3 rodzaje pytań</li>
				<li>- Pytania wyboru 1 z 4 </li>
				<li>- Uzupełnianie tekstów zdaniem najbardziej pasującym do kontektstu </li>
